/**
 * =================================================================================================================
 * 3. Explain the usage of strpos() and str_replace() functions in PHP.
 * =================================================================================================================
 */
/*
 - strpos() is used to find the position of the first occurrence of a substring within a string.
 - str_replace() is used to replace all occurrences of a substring with another substring within a string.
 */




/**
 * =================================================================================================================
 * 4. What is the difference between strcmp() and strcasecmp() functions?
 * =================================================================================================================
 */
/*
 - strcmp() is case-sensitive, meaning it considers the case of characters when comparing strings.
 - strcasecmp() is case-insensitive, meaning it ignores the case of characters when comparing strings.
 */




/**
 * =================================================================================================================
 * 5. How can you convert a string to uppercase and lowercase in PHP?
 * =================================================================================================================
 */
/*
 => You can use the strtoupper() function to convert a string to uppercase and the strtolower() function to convert a string to lowercase.
 */




/**
 * =================================================================================================================
 * 6. Explain the usage of explode() and implode() functions in PHP.
 * =================================================================================================================
 */
/*
 - explode() is used to split a string into an array based on a delimiter.
 - implode() (or join()) is used to join elements of an array into a string using a delimiter.
 */




/**
 * =================================================================================================================
 * 7. What is the purpose of the trim() function?
 * =================================================================================================================
 */
/*
 => The trim() function is used to remove whitespace or other predefined characters from the beginning and end of a string.
 */




/**
 * =================================================================================================================
 * 8. How can you extract a substring from a string in PHP?
 * =================================================================================================================
 */
/*
 => You can use the substr() function to extract a substring from a string based on its start position and length.
 */




